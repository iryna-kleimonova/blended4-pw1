const calculator = {
  a: null,
  b: null,

  read(a, b) {
    this.a = a;
    this.b = b;
  },

  exist() {
    return this.a !== null && this.b !== null;
  },

  sum() {
    if (this.exist()) {
      return this.a + this.b;
    } else {
      return 'No such properties';
    }
  },

  mult() {
    if (this.exist()) {
      return this.a * this.b;
    } else {
      return 'No such properties';
    }
  },
};

