function calcTotalPrice(fruits, fruitName) {
  let total = 0;

  for (const fruit of fruits) {
    if (fruit.name.toLowerCase() === fruitName.toLowerCase()) {
      total += fruit.price * fruit.quantity;
    }
  }

  if (total === 0) {
    return `Sorry! There is no fruit ${fruitName}`;
  }

  return total;
}

